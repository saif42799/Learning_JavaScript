// "continue"
for(let p = 1; p <= 20; p++){
    if(p == 13){
        // this skips over 13 and continues to 20
        continue;

    }
    else{
        console.log(p);
    } 
}


// "break"
for(let y = 1; y <= 20; y++){
    if(y == 13){
        // this stops when equal to 13
        break;

    }
    else{
        console.log(y);
    } 
}



