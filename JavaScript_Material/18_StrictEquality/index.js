// = assignment operator
// == camparison operator (compare if vales are equal)
// === strict equality operator (compare if values a nd datatype are equal)
// != inequality operator
// !== strict inequality operator 

// this is a number datatype
const PI = 3.14;


// this is comparing a number to a string
if(PI == "3.14"){
    console.log("That is Pi");
}
else{
    console.log("That is NOT Pi");
}


// this is comparing a number to a string, it has to to equal the same datatype (===))
if(PI === "3.14"){
    console.log("That is Pi");
}
else{
    console.log("That is NOT Pi");
}


// !=
if(PI != "3.14"){
    console.log("That is NOT Pi");
}
else{
    console.log("That is Pi");
}


// !=
if(PI !== 3.14){
    console.log("That is NOT Pi");
}
else{
    console.log("That is Pi");
}

